//  Definition for a binary tree node.
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

// Definition for a Node.
class Node {
public:
    int val;
    Node* next;
    Node* random;

    Node(int _val) {
        val = _val;
        next = NULL;
        random = NULL;
    }
};


class Solution {
public:
    bool wordBreak(string s, vector<string>& wordDict) {
        int index;
        string left, right;
        cout << s << "\n";
        if (s == "")return true;
        for (string str : wordDict)
        {
            index = s.find(str);
            if (index != EOF)
            {
                umap[str]++;
                left = s.substr(0, index);
                right = s.substr(index + str.length());

                if (umap[left] >=0 && wordBreak(left, wordDict))
                {
                    if (umap[right]>=0 && wordBreak(right, wordDict))
                        return true;
                    else
                    {
                        umap[right]--;
                    }
                }
                else
                {
                    umap[left]--;
                }
                cout << "fail s= " << s << "\n";
            }
        }
        umap[s]--;

        return false;
    }
    string& trim(string& s)
    {
        if (s.empty())
            return s;
        s.erase(0, s.find_first_not_of(" "));
        s.erase(s.find_last_not_of(" ") + 1);
        return s;
    }
private:
    unordered_map<string, int> umap;
};

